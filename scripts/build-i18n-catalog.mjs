import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.join(__dirname, '..');
const localizationsRoot = path.join(projectRoot, 'localizations');
const outputPath = path.join(projectRoot, 'src/i18n/catalog.generated.ts');

const SUPPORTED_LANGUAGES = ['en', 'tr', 'de', 'es', 'ar', 'ja', 'zh', 'id', 'pt', 'fr'];

function unescapeLocalizableString(value) {
  let output = '';
  let index = 0;

  while (index < value.length) {
    const char = value[index];

    if (char !== '\\') {
      output += char;
      index += 1;
      continue;
    }

    const next = value[index + 1];

    switch (next) {
      case 'n':
        output += '\n';
        break;
      case 'r':
        output += '\r';
        break;
      case 't':
        output += '\t';
        break;
      case '\\':
        output += '\\';
        break;
      case '"':
        output += '"';
        break;
      default:
        output += next ?? '';
        break;
    }

    index += 2;
  }

  return output;
}

function parseLocalizableStrings(source) {
  const entries = {};
  const pattern = /"((?:\\.|[^"\\])*)"\s*=\s*"((?:\\.|[^"\\])*)"\s*;/g;

  for (const match of source.matchAll(pattern)) {
    entries[unescapeLocalizableString(match[1])] = unescapeLocalizableString(match[2]);
  }

  return entries;
}

function loadLanguageCatalog(language) {
  const stringsPath = path.join(localizationsRoot, `${language}.lproj`, 'Localizable.strings');
  const source = readFileSync(stringsPath, 'utf8');

  return parseLocalizableStrings(source);
}

const catalogs = Object.fromEntries(
  SUPPORTED_LANGUAGES.map((language) => [language, loadLanguageCatalog(language)]),
);

const enKeys = Object.keys(catalogs.en).sort();

for (const language of SUPPORTED_LANGUAGES) {
  const languageKeys = Object.keys(catalogs[language]).sort();

  if (languageKeys.join('|') !== enKeys.join('|')) {
    const missing = enKeys.filter((key) => !(key in catalogs[language]));
    const extra = languageKeys.filter((key) => !(key in catalogs.en));

    throw new Error(
      [
        `Localization key mismatch for "${language}".`,
        missing.length > 0 ? `Missing: ${missing.join(', ')}` : null,
        extra.length > 0 ? `Extra: ${extra.join(', ')}` : null,
      ]
        .filter(Boolean)
        .join(' '),
    );
  }
}

mkdirSync(path.dirname(outputPath), { recursive: true });

const fileContents = `/* eslint-disable */
// Generated by scripts/build-i18n-catalog.mjs. Do not edit manually.

import type { AppLanguage } from '@/types/app-settings';

export type TranslationKey = ${enKeys.map((key) => JSON.stringify(key)).join(' | ')};

export const translationCatalogs: Record<AppLanguage, Record<TranslationKey, string>> = ${JSON.stringify(catalogs, null, 2)} as const;
`;

writeFileSync(outputPath, fileContents, 'utf8');

console.log(`Wrote ${outputPath}`);
